# 키워드파라미터 함수
# - 키 = 값 형태의 가변인자를 받아서 처리하는 함수
# - 선언형태 : def 함수명(**변수명):
# - 호출형태 : 함수명(문자열키='값, 문자열키=20)
# (!) 가변인자 사용할 시 key에 숫자는 사용 불가능!
#     ex) saveInfo(1="abc") --> 불가능
